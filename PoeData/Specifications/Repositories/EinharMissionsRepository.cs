    public bool TryGetManyById(string? key, out IReadOnlyList<EinharMissionsDat> items)
    {
        if (key is null)
        {
            items = Array.Empty<EinharMissionsDat>();
            return false;
        }

        if (byId is null)
        {
            byId = new();
            foreach (var item in Items)
            {
                var itemKey = item.Id;

                if (!byId.TryGetValue(itemKey, out var list))
                {
                    list = new();
                    byId.TryAdd(itemKey, list);
